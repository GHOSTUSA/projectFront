<script lang="ts" setup>
import { useAuthStore } from "~/stores/authentification/AuthStore";

const authStore = useAuthStore();

const isAuthenticated = computed(() => authStore.isAuthenticated);
const isAdmin = computed(() => authStore.user?.role === "admin");

watch(
  [isAuthenticated, isAdmin],
  ([auth, admin]) => {
    if (auth && !admin) {
      navigateTo("/restaurant");
    } else if (!auth) {
      navigateTo("/");
    }
  },
  { immediate: true }
);

function logout() {
  authStore.logout();
  navigateTo("/");
}
</script>

<template>
  <div class="admin-layout">
    <header v-if="isAuthenticated && isAdmin" class="admin-header">
      <nav class="admin-nav">
        <div class="nav-brand">
          <h2>Administration</h2>
        </div>
        <ul class="nav-menu">
          <li><NuxtLink to="/Admin/backOffice">Dashboard</NuxtLink></li>
          <li>
            <NuxtLink to="/Admin/backOffice/restaurants">Restaurants</NuxtLink>
          </li>
          <li>
            <NuxtLink to="/Admin/backOffice/commands">Commandes</NuxtLink>
          </li>
          <li class="user-info">
            <span
              >{{ authStore.user?.firstName }}
              {{ authStore.user?.lastName }}</span
            >
          </li>
          <li>
            <button @click="logout" class="logout-btn">DÃ©connexion</button>
          </li>
        </ul>
      </nav>
    </header>
    <main class="admin-main">
      <slot />
    </main>
  </div>
</template>

